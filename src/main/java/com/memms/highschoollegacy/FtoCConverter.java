package com.memms.highschoollegacy;

public class FtoCConverter {

	public static void main(String args []) {
		
		int f = 212;
		
		System.out.println("Celcius to Farenheit formula: (F - 32)*5/9 = C");
//		System.out.println((f - 32)*5/9);
		System.out.print("212 degrees F converts to " + ((f - 32)*5/9) + " degrees C.");
	}
}
