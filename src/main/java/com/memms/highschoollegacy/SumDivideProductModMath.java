package com.memms.highschoollegacy;

public class SumDivideProductModMath {
	public static void main(String args []) {
	
		double a = 3.0;
		double b = 1.0;
		
		System.out.println(a + " + " + b + " = " + (a+b));
		System.out.println(a + " - " + b + " = " + (a-b));
		System.out.println(a + " / " + b + " = " + (a/b));
		System.out.println(a + " % " + b + " = " + (a%b));

	}
}
